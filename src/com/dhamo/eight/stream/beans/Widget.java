package com.dhamo.eight.stream.beans;

public class Widget {
    String colour;
    int weight;

    public Widget() {
    }

    public Widget(String colour, int weight) {
        this.colour = colour;
        this.weight = weight;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
